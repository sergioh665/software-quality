// Função 1 - soma de dois números
function soma(a, b) {
    return a + b;
}

// Função 2 - soma de dois números (duplicada)
function somaDuplicada(a, b) {
    return a + b;
}

// Função 3 - cálculo da área de um retângulo
function areaRetangulo(largura, altura) {
    return largura * altura;
}

// Função 4 - cálculo da área de um quadrado (duplicado)
function areaQuadrado(lado) {
    return lado * lado;
}

// Função 5 - multiplicação de dois números
function multiplicacao(a, b) {
    return a * b;
}

// Função 6 - multiplicação de dois números (duplicada)
function multiplicacaoDuplicada(a, b) {
    return a * b;
}

// Função 7 - cálculo do volume de um cubo
function volumeCubo(lado) {
    return lado * lado * lado;
}

// Função 8 - cálculo do volume de um cubo (duplicada)
function volumeCuboDuplicado(lado) {
    return lado * lado * lado;
}

console.log(soma(5, 10));
console.log(areaRetangulo(5, 10));
console.log(multiplicacao(3, 4));
console.log(volumeCubo(3));
